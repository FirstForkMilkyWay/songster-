class Songs

    attr_accessor :name, :artist, :tab_types

    def initialize
      @name = name
      @artist = artist
      @tab_types = tab_types
    end

    def name=(name)
      @name = name
    end

    def artist=(artist)
      @artist = artist
    end

    def artist
      @artist
    end

    def tab_types=(tab_types)
      @tab_types = tab_types
    end

    def tab_type
      @tab_type
    end 





end
